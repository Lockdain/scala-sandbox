    def pow(x: Int, n: Int): Double = {
      if (n == 0) 1
      else if (n < 0) 1 / pow(x, -n)
      else if (n % 2 != 0) x * pow(x, n - 1)
      else {
        val y = pow(x, n / 2).asInstanceOf[Double]
      }

  }
}
